#include <iostream>
using namespace std;

int main() {    
    int num = 9;
    
    if (num == 10){
        cout << "first output" << endl;
    } else if (num == 9){
        cout << "second output" << endl;
    } else {
        cout << "last output" << endl;
    }
    
    return 0;
}

// Operators:
// == means equal to
// != means not equal to
// > means greater than
// < means less than
// >= means greater than or equal to
// <= means less than or equal to